select w.EMPLOYEE_ID,w.FIRST_NAME,w.LAST_NAME,w.MANAGER_ID,m.EMPLOYEE_ID, m.FIRST_NAME,m.LAST_NAME
from EMPLOYEES W join EMPLOYEES M
